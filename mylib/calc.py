"""
A calculator module

"""

def add(a, b):
    """
    Add two numbers

    """
    return a + b
def sub(a, b):
    """
    Subtract two numbers

    """
    return a - b
def mul(a, b):
    """
    Multiply two numbers

    """
    return a * b
def div(a, b):
    """
    Divide two numbers

    """
    return a / b

#build a function to calculate the power of a number with x as the base and y as the exponent. The function should return the result of the calculation.
def power(x, y):
    """
    Power of a number

    """
    return x ** y

